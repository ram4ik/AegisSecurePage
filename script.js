    function applyTranslations(languageCode) {
        const language = TRANSLATIONS[languageCode] ? languageCode : 'en';
        const dictionary = TRANSLATIONS[language];

        document.documentElement.lang = language;
        document.title = dictionary.pageTitle;

        const descriptionMeta = document.querySelector('meta[name="description"]');
        if (descriptionMeta) {
            descriptionMeta.setAttribute('content', dictionary.pageDescription);
        }

        const languageButton = document.querySelector('#language-toggle');
        if (languageButton) {
            languageButton.textContent = dictionary.languageButton;
        }

        const languageLabel = document.querySelector('#language-label');
        if (languageLabel) {
            languageLabel.textContent = dictionary.languageLabel;
        }

        translationBindings.forEach(binding => {
            const element = document.querySelector(binding.selector);
            if (!element || !dictionary[binding.key]) {
                return;
            }
            if (binding.html) {
                element.innerHTML = dictionary[binding.key];
            } else {
                element.textContent = dictionary[binding.key];
            }
        });

        localStorage.setItem('aegis-language', language);
    }

    const languageSelect = document.getElementById('language-select');
    const languageToggle = document.getElementById('language-toggle');
    const savedLanguage = localStorage.getItem('aegis-language') || 'en';
    languageSelect.value = TRANSLATIONS[savedLanguage] ? savedLanguage : 'en';
    applyTranslations(languageSelect.value);

    languageToggle.addEventListener('click', function() {
        languageSwitcher.classList.toggle('open');
    });

    languageSelect.addEventListener('change', function(event) {
        applyTranslations(event.target.value);
        languageSwitcher.classList.remove('open');
    });
